<script>
  document.addEventListener("DOMContentLoaded", function () {
    const fadeBox = document.querySelector(".fade-box");
    const fade01Elements = document.querySelectorAll(".fade01");
    const fade02Elements = document.querySelectorAll(".fade02");
    let fade01Triggered = false;
    let fade02Triggered = false;

    function fadeUpElements(elements) {
      elements.forEach(element => {
        element.classList.add("fade-up");
      });
    }

    function fadeUpElementsMo(element) {
        element.classList.add("fade-up");
    }

    function onScroll() {
      const fadeBoxRect = fadeBox.getBoundingClientRect();
      const windowHeight = window.innerHeight;

      if (fadeBoxRect.top + 300 < windowHeight && fadeBoxRect.bottom > 0) {
        if (!fade01Triggered) {
          fade01Triggered = true;

          if(window.innerWidth > 1024) {
              fadeUpElements(fade01Elements);
    
              setTimeout(() => {
                fade02Triggered = true;
                fadeUpElements(fade02Elements);
              }, 500);
          } else {
            document.querySelectorAll('.mo-fade').forEach((el, idx) => {
                if(idx == 0) {
                    fadeUpElementsMo(el);
                } else {
                    setTimeout(() => {
                        fadeUpElementsMo(el);
                    }, (500 * idx));
                }
                fade02Triggered = true;
            });
          }
        }
      }
    }

    window.addEventListener("scroll", onScroll);
  });
</script>
